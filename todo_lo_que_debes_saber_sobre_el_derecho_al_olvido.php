                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Gracias a las redes sociales y motores de búsqueda, nuestra información está expuesta al público en todo momento, y es que,  todos hemos querido compartir algo de nuestra vida social, como los lugares que visitamos, personas que frecuentamos o lo que hacemos a diario. Hasta ahí todo está bien, pero, alguna vez te has preguntado si tu información fuera usada de forma inadecuada con el fin de dañar tu imagen, ¿qué harías? O sí, existiera una información errónea que le está causando daños y perjuicios a tu buen nombre, ¿cómo podrías eliminarla? A continuación te vamos a explicar de qué se trata el derecho al olvido o el derecho a eliminar datos de internet,  que no son ciertos, o que si lo son y ya no están vigentes o caducaron. 
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">Qué es el derecho al olvido</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    El derecho al olvido se define como el derecho que tienen las personas de eliminar alguna información de hechos pasados de los buscadores de internet, es decir, el derecho de restablecer la imagen o buen nombre de una persona en la red, al borrar datos que no son ciertos, o que, de ser ciertos, ya fueron resarcidos o caducados. De esta manera el ciudadano puede volver a disfrutar de los beneficios que ofrece la web sin que se vea afectada su imagen o su honorabilidad. 
                                                    </p>
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">A quién está dirigido</h3>
                                                    <p class="flv_style_4" style="text-align: justify; margin: 0 40px 15px;">
                                                    El derecho al olvido puede ser ejercido por cualquier  persona o empresa que esté viviendo alguna de las situaciones que se mencionan a continuación.   
                                                    </p>
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <ul class="flv_style_4">
                                                    <li><h4 style="margin: 0 40px 15px;">Casos de Ciberbullying </h4>
                                                            
                                                            <p style="margin: 0 40px 15px;">
                                                            Sucede cuando alguna foto o video de un usuario de internet es utilizada para ser viralizado en la red, en forma de acoso o ciberbullying, haciendo con esto que la imagen de la persona se vea vulnerada. Pero eso no es todo, esta información continúa en la web hasta que el usuario afectado decida eliminarla. 
                                                            <p></li>
                                                        
                                                    <li><h4 style="margin: 0 40px 15px;">Uso de información errada para fines de desprestigio</h4>
                                                            
                                                            <p style="text-align: justify;margin: 0 40px 15px;">
                                                            En algunas ocasiones los usuarios de la web  generalmente empresas, se ven inmiscuidos en conflictos de información errada o noticias falsas, por dar un ejemplo: La empresa  “Tour y Tour” vende tiquetes de viaje a “Luis”  y “Luis” resulta siendo un delincuente, luego aparece una noticia en los principales portales de internet diciendo que a “Luis” le fue vendido  el  tiquete por la agencia “tour y tour”, que nada tiene que ver con el delito de “Luis”, en algunos casos, incluso estas empresas son investigadas sin tener nada que ver con los delincuentes a causa de este tipo de noticias. 
                                                            </br>
                                                            Esta situación trae demasiados perjuicios,  en este caso, a esta empresa por información errónea que ronda en internet y que por supuesto, el propietario no quiere que vean sus clientes por obvias razones.
                                                            </p></li>

                                                    <li><h4 style="margin: 0 40px 15px;">Penas ya cumplidas </h4>

                                                            <p style="margin: 0 40px 15px;">
                                                            Las personas que han cumplido penas en prisión, al salir tienen derecho a restablecer su vida y a que su historial sea borrado de internet, en estos casos, se revisa el proceso a fondo y se realiza la correspondiente  eliminación de los datos a los que  haya lugar. 
                                                            </p></li>
                                                       
                                               </ul>
