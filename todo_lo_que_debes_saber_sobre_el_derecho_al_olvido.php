                                                <h3 style="margin: 0 40px 15px;">¿Cómo ejercer el derecho al olvido?</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Ejercer el derecho al olvido puede parecer sencillo, sin embargo, lo mejor es que tu situación sea evaluada por un abogado, para que tengas certeza que tu caso es procedente. Luego  estos mismos se encargan de realizar la respectiva solicitud a los motores de búsqueda ya sea en Google o en el buscador en el que aparece dicha información. Después de esto, el buscador  revisa esta solicitud y realiza las investigaciones a las que haya lugar y finalmente aprueban o no la eliminación del contenido. En Europa se trata de un derecho de protección de datos por lo que, al día de hoy se han radicado cientos de miles de solicitudes y han sido ejecutadas con éxito.   
                                                    </p>
