                                                    <ul style="margin: 0 40px 15px;" class="flv_style_4">
                                                        <li style="margin: 0 40px 15px;">Eliminar la información solicitada de los resultados mostrados por Google u otros motores de búsqueda. </li>
                                                        <li style="margin: 0 40px 15px;">Restablecer la imagen y el buen nombre de las personas implicadas.</li>  
                                                        <li style="margin: 0 40px 15px;">Da la posibilidad de recrear información cierta sobre una empresa o persona. </li>  
