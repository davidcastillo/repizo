                                            <h3  style="margin: 0 40px 15px;"class="title"><span id="faq_3">Preguntas frecuentes</span></h3>
                                            <div class="mfn-acc faq_wrapper ">
                                                <div style="margin: 0 40px 15px;"class="question">
                                                    <div class="title">
                                                        <span class="num">1</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Cuánto tiempo demora esta solicitud?
                                                    </div>
                                                    <div class="answer">
                                                    Como tal no se trata de tiempo, se trata de la veracidad del contenido a investigar. Es decir, el motor de búsqueda recibe la solicitud y empieza a realizar una exhaustiva investigación para determinar si realmente la solicitud procede. Entonces, si son varios enlaces la investigación podría demorarse un poco más. Sin embargo lo importante es empezar la solicitud lo antes posible 
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">2</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Es posible que eliminen toda la información que necesito?
                                                    </div>
                                                    <div class="answer">
                                                    Si no se elimina toda la información, por lo menos sí una buena parte, pues en algunas ocasiones las solicitudes son improcedentes, pero en la mayoría de los casos puedes estar tranquilo que se realizará el proceso correspondiente por tratarse de un tema legal. Es decir, es un derecho que puedes ejercer y que probablemente fallará a tu favor.
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">3</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Cómo puede un abogado ayudarme a realizar la solicitud?
                                                    </div>
                                                    <div class="answer">
                                                    Ellos se encargarán de recibir la solicitud y revisar el enlace de la  información  en cuestión en los portales de internet, luego si esta procede,  realizarán la solicitud en el buscador que corresponda. Después de radicada la solicitud se hará seguimiento al proceso  hasta obtener la respuesta del caso. 
