                                                <h3 style="margin: 0 40px 15px;" >En resumen</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    EL derecho al olvido es una opción que ofrece la ley para restablecer nuestra imagen en internet, solucionando con esto el resarcimiento de nuestro nombre  e información en la web. Que,  de no existir este derecho que protege la información de los usuarios en internet, podría ocasionar muchos perjuicios morales, económicos, laborales entre otros. Así que si en este momento tienes una situación como las que hemos mencionado antes, no dudes en contactar a nuestro grupo de abogados especializados que están siempre prestos a  ayudarte.   
