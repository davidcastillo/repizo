                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Los usuarios colombianos de PayPal, tienen un canal de transacciones electrónico que podría resultar favorable si realizan operaciones internacionales. De lo contrario e infelizmente los costos asociados a comisiones son muy altos, y ya que esto actualmente no se encuentra regulado en ningún ministerio, es muy probable que no se reglamente el uso nacional en poco tiempo, que de llegar a ser posible , le permitiría a los usuarios realizar más transacciones de una forma fácil rápida y segura.   
