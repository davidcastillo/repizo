                                                <h3 style="margin: 0 40px 15px;" >Cómo retirar fondos en Colombia</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    En este momento en Colombia existen dos opciones: Bancolombia a través de su aplicación Nequi y Davivienda. Y aunque estos bancos son los más usados en Colombia actualmente, también deja por fuera a miles de usuarios del sistema bancario que no tienen convenio con PayPal. 
                                                    </br>
                                                    Desafortunadamente para los colombianos este canal de pagos es bastante limitado, además de esto, por el hecho de solo permitir pagos  a nivel internacional, los costos en comisiones son más altos o los cobros a los compradores extranjeros es también mayor, lo que significa por ende una baja en la venta de los productos que van a ser exportados.    
                                                    </p>
