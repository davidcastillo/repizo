                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    En la actualidad el mundo se mueve a través del comercio electrónico, y no es para menos, estamos en la era de la información y la comunicación, y prácticamente todo lo hacemos por internet, sea en un computador o en un dispositivo móvil. 
Gracias a esto se puede hacer cualquier tipo de transacción en línea: comprar, vender, hacer negocios, subastar, participar de transacciones interesantes etc. Todo lo anterior puede ser posible desde que se crearon las plataformas de pagos electrónicos, y cada vez, son más frecuentes y más comunes. 
Vamos a hablar específicamente de la pasarela pagos PayPal, que permite realizar de forma muy sencilla transacciones en línea, y de las ventajas y desventajas de poseer una cuenta. 

                                                </div>
