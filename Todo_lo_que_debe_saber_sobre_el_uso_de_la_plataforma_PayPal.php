                                            <h3  style="margin: 0 40px 15px;"class="title"><span id="faq_3">Preguntas frecuentes</span></h3>
                                            <div class="mfn-acc faq_wrapper ">
                                                <div style="margin: 0 40px 15px;"class="question">
                                                    <div class="title">
                                                        <span class="num">1</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Dónde puedo reclamar si tengo problemas con PayPal?
                                                    </div>
                                                    <div class="answer">
                                                    En la plataforma existe un canal de servicio al cliente, sin embargo este canal es bastante limitado, y las reclamaciones pueden tardar mucho tiempo en resolverse. 
                                                    <br/>
                                                    Debido a que este servicio es deficiente, y cuando se trata de tu dinero, se pueden instaurar acciones legales que te permiten realizar una reclamación más seria, en la que las posibilidades de resultar favorables son muy amplias.  Por ejemplo, cuando realizas una compra en el exterior y el proveedor no cumple con el producto o servicio, reclamas para que tu dinero sea devuelto.
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">2</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Qué puedo hacer si tengo inconvenientes con clientes o proveedores extranjeros en mis pagos ?
                                                    </div>
                                                    <div class="answer">
                                                    En teoría PayPal se encarga de comprobar que todos los usuarios de las cuentas registradas sean clientes y proveedores reales. En el caso en que se llegue a presentar un movimiento sospechoso o inusual, PayPal retiene el dinero hasta que dicha situación se aclare. Si esto no sucede es necesario ir a instancias legales en la cual se reclamará por vías jurídicas, y esto resulta más eficiente.
