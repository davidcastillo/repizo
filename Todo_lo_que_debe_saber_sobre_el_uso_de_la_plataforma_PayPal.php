                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">Qué es Paypal</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    PayPal es hoy por hoy una de las plataformas para transar dinero online más importante del mundo, fue creada en 1998 y abrió un sinnúmero de posibilidades a los pagos, compra y venta para los usuarios de internet. 
Esta plataforma actualmente gana  más de $1.000  millones de  dólares al año, ya que cobra comisiones al realizar las transacciones por internet. Existe un área para clientes naturales y otra para negocios, así la posibilidad de realizar pagos, compra y venta es muy amplia, tiene presencia en más de 190 países. Sus sedes principales están en California, Nebraska y Dublín, lo que significa que se rigen con las normas legales de Estados Unidos y la Unión Europea, no exactamente como los bancos, pero sí realizando verificaciones para evitar el lavado de activos.     
 
                                                    </p>
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">Cómo funciona PayPal </h3>
                                                    <p class="flv_style_4" style="text-align: justify; margin: 0 40px 15px;">
                                                    El usuario de PayPal debe crear una cuenta asociando su correo electrónico, allí se deposita el dinero de pagos y luego ese dinero puede ser usado para compras por internet. Para realizar pagos  a otros usuarios, debes asociar tu correo con una tarjeta débito o crédito.  Vamos a darte un ejemplo de cómo funciona tu dinero en PayPal:
                                                    Realizas un trabajo como freelance, tu jefe te paga a través de PayPal, ahora tienes tu dinero en tu cuenta asociada a tu correo electrónico, entonces, deseas comprar un libro por amazon, y pagas desde tu cuenta. Es muy fácil.    
                                                    </p>
                                                </div>
                                            </div>
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">Cómo funciona PayPal en Colombia</h3>
                                                    <p class="flv_style_4" style="text-align: justify; margin: 0 40px 15px;">
                                                    Inicialmente en Colombia se pensó usar PayPal para realizar todo tipo de transacciones, tal y como se hace a nivel mundial. Pero debido a la normatividad local que no permite realizar ciertas transacciones a nivel nacional, el uso fue relegado solamente a transacciones internacionales, esto quiere decir que; los usuarios de PayPal pueden hacer pagos o compras en línea a tiendas solamente en el exterior, o pueden únicamente vender a través de esta plataforma a clientes internacionales. 
                                                    </p>
                                                </div>
                                            </div>
