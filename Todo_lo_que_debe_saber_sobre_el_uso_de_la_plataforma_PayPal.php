                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">Ventajas y desventajas </h3>
                                                    <p class="flv_style_4" style="text-align: justify; margin: 0 40px 15px;">
                                                    Si bien es sabido que que el comercio electrónico es el presente y futuro de esta generación de usuarios de internet, debemos tener en cuenta algunos pros y contras de esta plataforma específicamente para usuarios en Colombia: 
                                                    </p>
                                                </div>
                                            </div>
                                                <h3 style="margin: 0 40px 15px;">Ventajas</h3>
                                                <ul class="flv_style_4">
                                                    <li style="margin: 0 40px 15px;">Fácil de usar a través de sus plataformas web.</li>
                                                        
                                                    <li style="margin: 0 40px 15px;">Tienes dinero disponible para realizar compras por internet en comercios internacionales.</li>

                                                    <li style="margin: 0 40px 15px;">Puedes asociar tu cuenta con tus tarjetas de credito y debito y es seguro.</li>

                                                    <li style="margin: 0 40px 15px;">Puedes disponer de tu efectivo si tienes cuentas en Bancolombia o Davivienda. </li>
                                                       
                                               </ul>
                                               </div>
                                               <h3 style="margin: 0 40px 15px;">Desventajas</h3>
                                                <ul class="flv_style_4">
                                                    <li style="margin: 0 40px 15px;">No puedes usar tu dinero en PayPal para pagos o compras nacionales. </li>
                                                        
                                                    <li style="margin: 0 40px 15px;">Los costos de comisiones en  pago pueden ser muy elevados.</li>

                                                    <li style="margin: 0 40px 15px;">El servicio al cliente es deficiente, no hay contactos telefónicos y a través de chat puede resultar engorroso realizar algún tipo de reclamación.</li>

                                                    <li style="margin: 0 40px 15px;">La plataforma puede retener el dinero si sospecha de algún movimiento inusual, y puede tardar varios días en entregarlo. </li>

                                                    <li style="margin: 0 40px 15px;">No hay convenios con todos los bancos en Colombia.</li>
                                                       
                                               </ul>
