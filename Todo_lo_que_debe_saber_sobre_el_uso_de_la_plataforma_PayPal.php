                                                <h3 style="margin: 0 40px 15px;">Que significa para los comerciantes colombianos no poder realizar transacciones a través de paypal</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Cerrar este canal transaccional significa para los colombianos no poder mover el dinero que reciben en su cuenta proveniente del exterior para compras nacionales. 
Por ejemplo; el freelance que trabaja para una empresa en el exterior solo puede gastar su dinero en compras internacionales, si desea apoyar una empresa de zapatos local tendría que retirar los fondos de otra forma y luego comprar a nivel nacional. Sin embargo esto implicaría un cobro de comisiones elevado e injusto.
   
                                                    </p>
