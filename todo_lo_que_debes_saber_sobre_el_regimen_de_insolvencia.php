                                            <h3  style="margin: 0 40px 15px;"class="title"><span id="faq_3">Preguntas frecuentes</span></h3>
                                            <div class="mfn-acc faq_wrapper ">
                                                <div style="margin: 0 40px 15px;"class="question">
                                                    <div class="title">
                                                        <span class="num">1</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Cuánto tiempo demora esta solicitud?
                                                    </div>
                                                    <div class="answer">
                                                    El tiempo de esta solicitud depende de los acuerdos de pagos a los que lleguen las partes del negocio, es decir, si se establece rápidamente quienes son los acreedores y se pacta un nuevo término en los pagos, podría ser más ágil el proceso.
                                                    </br>
                                                    En el caso contrario,  si las partes no están de acuerdo con las nuevas propuestas de pagos, el proceso podría demorarse un poco más. Lo más importante en estos casos es empezar el proceso lo más rápido posible para evitar que las deudas sigan creciendo.  
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">2</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Cuántos tipos de procesos de insolvencia existen en Colombia?
                                                    </div>
                                                    <div class="answer">
                                                    Existen dos tipos de procesos que son la reorganización empresarial que es, la que pretende preservar empresas viables ayudándoles a regular sus actividades económicas y de crédito, y evitar a toda costa la liquidación.
                                                    </br>
                                                    El segundo tipo es la liquidación judicial; este se trata de la liquidación de la empresa de una forma ordenada con el fin de aprovechar el patrimonio del empresario.
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">3</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Cómo puede un abogado ayudarme a realizar la solicitud?
                                                    </div>
                                                    <div class="answer">
                                                    En este tipo de situaciones la labor de los abogados será la de recoger toda la información de la empresa y realizar las propuestas correspondientes y además de ello presentar el caso ante la autoridad competente, en este caso la Superintendencia de sociedades o el Juez Civil del circuito. Así como también será el  abogado quien realice el seguimiento correspondiente del caso. 
                                                    </div>
