                                        <div class="faq">
                                            <h3  style="margin: 0 40px 15px;"class="title"><span id="faq_3">Preguntas frecuentes</span></h3>
                                            <div class="mfn-acc faq_wrapper ">
                                                <div style="margin: 0 40px 15px;"class="question">
                                                    <div class="title">
                                                        <span class="num">1</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Qué características debe tener mi empresa para empezar a licitar en contrataciones públicas?
                                                    </div>
                                                    <div class="answer">
                                                    Lo más importante es que la empresa esté legalmente constituida e inscrita en la cámara de comercio, además  que esté al día en impuestos y todo lo que requiere la ley. Tu empresa puede ser pequeña, mediana o grande empresa. En lo posible tener vida crediticia, experiencia en el ramo en el que desea contratar. Luego la agencia de contratación te exigirá otros requisitos para seguir con el concurso.
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">2</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Las empresas extranjeras pueden aplicar a contrataciones con el estado colombiano?
                                                    </div>
                                                    <div class="answer">
                                                    En el pasado la mayoría de los contratos eran adjudicados a empresas extranjeras, y hoy por hoy se siguen recibiendo propuestas de empresas del exterior, sin embargo es precisamente este sistema de implementación de Pliegos Tipo, que permiten que también empresas colombianas entren en los concursos de licitación estatal. Actualmente las empresas extranjeras siguen licitando, pero están en desventaja con las empresas colombianas, a menos que tengan mejores propuestas.
                                                    </div>
                                                </div>
                                                <div style="margin: 0 40px 15px;" class="question">
                                                    <div class="title">
                                                        <span class="num">3</span><i class="icon-plus acc-icon-plus"></i><i class="icon-minus acc-icon-minus"></i>¿Cómo puedo empezar a realizar este tipo de contratos?
                                                    </div>
                                                    <div class="answer">
                                                    Las contrataciones con el estado pueden resultar muy atractivas económicamente hablando, y de darse una contratación estatal es muy posible seguir aplicando a los contratos y ganar buenos dividendos, sin embargo puede resultar un poco engorroso el proceso de recolección de documentos y la realización de  todo el proceso de principio a fin. Sin embargo existen abogados especializados en este tipo de procesos, ya que conocen todo el flujograma de procedimientos para realizar una licitación estatal con éxito, esta es una opción muy válida si se quiere empezar este camino.
                                                    </div>
