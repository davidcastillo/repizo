                                            <h2 style="text-align:center">Todo lo que debe saber sobre documentos y Pliegos Tipo en la contratación estatal</h2>
                                            <div class=""></div><img  style=" display: block; margin-left: auto;  margin-right: auto; width: 50%;" width="800" height="380" src="images/documentos_contratación_abogados_huila.jpg" class="scale-with-grid wp-post-image" alt="beauty_portfolio_2" />
                                            </div>
                                    </div>
                                </div>

                                                    
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Las empresas privadas dedicadas a construcción u obras civiles, generalmente quieren licitar con el estado y ganar proyectos que les permitan obtener buenos dividendos y con ello seguridad económica tanto a los empleados como a los propietarios. Sin embargo, a raíz de los desafortunados casos de corrupción en los que se han visto implicados varias empresas en el país, la presidencia de la república ha decidido estandarizar los procesos en la contratación de obras públicas. En este artículo le contaremos todo acerca de la contratación estatal y los documentos y pliegos tipo que debe presentar para licitar con el estado. 
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">¿Qué son los Pliegos Tipo en la contratación estatal?</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Los documentos y Pliegos Tipo de la contratación estatal, son documentos de obligatorio cumplimiento para todos los participantes de una licitación pública. Está establecido en la Ley 2022 DE 2020 - <a href="images/Resolucion 161 de 2021.pdf" target="_blank" rel="Abogado Huila">Resolucion 161 de 2021</a> y se trata básicamente de   formatos que fueron establecidos por la presidencia de la república y La Agencia Nacional de Contratación Pública Colombia Compra Eficiente, con el fin único de que todos los participantes, tengan igualdad de condiciones a la hora de aplicar a proyectos públicos y además de esto, el proceso de contratación sea más transparente y tengan mejor trazabilidad. Así las cosas y con el debido cumplimento, todos los licitantes, tienen los mismos deberes y derechos para la  participación en contratos estatales. 
                                                    </p>
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">¿Cuándo se requiere usar los Pliegos Tipo? </h3>
                                                    <p class="flv_style_4" style="text-align: justify; margin: 0 40px 15px;">
                                                    Se requiere usar los Pliegos Tipo, siempre que se quiera hacer una contratación con el estado, es decir licitaciones públicas para obras de infraestructura de transporte. Por lo tanto estos documentos serán revisados y evaluados para verificar el estricto cumplimiento de todas las normas y procedimientos que en ellos está establecido.
                                                    </p>
                                                    <p class="flv_style_4" style="text-align: justify; margin: 0 40px 15px;">
                                                    Esto con el fin de que todos los participantes tengan igualdad de condiciones a la hora de licitar con el estado. Estos documentos resultan de vital importancia para evitar procesos indebidos, ya que con la correcta utilización de los formatos, se puede evidenciar que todos los participantes de una licitación cumplan con los requerimientos, sin ellos, sería muy difícil verificar que realmente quien contrata, cumpla con lo que ha ofrecido y se hace más difícil entablar una reclamación. etc.
                                                    </p>
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">¿Qué entidades deben utilizar los Pliegos Tipo?</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Todas las entidades estatales  que realizan contratos para obras de infraestructura de transporte. Hoy por hoy algunas entidades del estado como el Instituto de Desarrollo Urbano IDU, el Instituto de Infraestructuras y concesiones  de Cundinamarca ICCU, algunas alcaldías entre otros, están usando estos documentos Tipo  para mejorar los procesos de contratación, por lo tanto así también deberá someterse a estos estatutos las empresas que quieran licitar en obras de infraestructura de transporte y otras obras estatales al cumplimiento de la documentación tipo requerida por determinada entidad pública.
                                                    </p>
                                                </div>
                                            </div>
                                            <!-- One full width row-->
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;">¿Cuáles son los documentos tipo que se usan actualmente?</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Para llevar a  cabo los procesos licitatorios con entidades públicas, dependiendo del tipo de contratación deberá exigir los siguientes tipos de documentos:
                                                    </p>
                                                <ul class="flv_style_4">
                                                    <li><h4 style="margin: 0 40px 15px;">Documentos base:</h4>
                                                            
                                                            <p style="text-align: justify;margin: 0 40px 15px;">
                                                            Se trata de varios documentos diligenciados en formatos en los cuales se pretende presentar la empresa, presentar la propuesta, demostrar  la experiencia de la empresa, demostrar la situación económica, evidenciar el pago de  parafiscales a sus empleados, entre otros.
                                                            </br>
                                                            Estos documentos base establecen los procesos del concurso, y  al  ser más completos, tienen una puntuación importante en el caso en que haya más oferentes.
                                                            </p></li>

                                                    <li><h4 style="margin: 0 40px 15px;">Anexos </h4>

                                                            <p style="margin: 0 40px 15px;">
                                                            todo tipo de información relevante que requiera el proceso, por ejemplo experiencias laborales de los empleados, si se trata de obras de infraestructura se deberá presentar fotos de los proyectos, planos, cartas, etc
                                                            </p></li>
                                                       
                                               </ul>
                                               <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                               Otro tipo de documentos pueden ser formatos, matrices o formularios que se exigen dependiendo del concurso al que se está aplicando.
                                               </br>
                                               Para ello entonces tanto las empresas públicas como los oferentes deben estar en la capacidad de cumplir a cabalidad con todos los requisitos que están establecidos en la ley.                                                </p>
                                               </div>
                                            </div>
                                            <!-- One full width row-->
                                            <!-- One full width row-->
                                            <div class="column one column_column">
                                                <div class="column_attr ">
                                                <h3 style="margin: 0 40px 15px;" >En resumen</h3>
                                                    <p class="flv_style_4" style="text-align: justify;margin: 0 40px 15px;">
                                                    Los Pliegos Tipo, son una excelente posibilidad para todas las empresas que deseen realizar contratos con el estado, ya que gracias a estos formatos es posible la aplicación para cualquier tipo de empresas, pues esto constituye un concurso más transparente y eficiente, tanto para la entidad pública como para el empresario licitador. Es posible que más pequeñas y medianas empresas, puedan acceder a este tipo de contratos en los cuales los beneficios económicos son importantes para la  mejora,  acreditación y reputación de las empresas y de los empleados, pues el concurso está abierto a pequeñas, medianas y grandes empresas colombianas y del exterior.   
                                                    </p>
                                                </div>
                                            </div>
